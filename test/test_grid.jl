            """
            1┬────────┬────────┬────────┐
             │        │        │        │
             │ (1, 4) │ (2, 4) │ (3, 4) │
             │        │        │        │
            4┼────────┼────────┼────────│
             │        │        │        │
             │ (1, 3) │ (2, 3) │ (3, 3) │
             │        │        │        │
            3┼────────┼────────┼────────│
             │        │        │        │
             │ (1, 2) │ (2, 2) │ (3, 2) │
             │        │        │        │
            2┼────────┼────────┼────────│
             │        │        │        │
             │ (1, 1) │ (2, 1) │ (3, 1) │
             │        │        │        │
            1┼────────┼────────┼────────┤
             1        2        3        1
            """
            N = (3, 4)
            d = size(N, 1)
            dims = Tuple(1:n for n in N)
            linear = LinearIndices(dims)
            @test cell_vertices(CartesianIndex(1, 1), N) ==
                  map(CartesianIndex, [(1, 1) (1, 2); (2, 1) (2, 2)])
            @test cell_vertices(CartesianIndex(2, 1), N) ==
                  map(CartesianIndex, [(2, 1) (2, 2); (3, 1) (3, 2)])
            @test cell_vertices(CartesianIndex(3, 1), N) ==
                  map(CartesianIndex, [(3, 1) (3, 2); (1, 1) (1, 2)])
            @test cell_vertices(CartesianIndex(1, 2), N) ==
                  map(CartesianIndex, [(1, 2) (1, 3); (2, 2) (2, 3)])
            @test cell_vertices(CartesianIndex(2, 2), N) ==
                  map(CartesianIndex, [(2, 2) (2, 3); (3, 2) (3, 3)])
            @test cell_vertices(CartesianIndex(3, 2), N) ==
                  map(CartesianIndex, [(3, 2) (3, 3); (1, 2) (1, 3)])
            @test cell_vertices(CartesianIndex(1, 3), N) ==
                  map(CartesianIndex, [(1, 3) (1, 4); (2, 3) (2, 4)])
            @test cell_vertices(CartesianIndex(2, 3), N) ==
                  map(CartesianIndex, [(2, 3) (2, 4); (3, 3) (3, 4)])
            @test cell_vertices(CartesianIndex(3, 3), N) ==
                  map(CartesianIndex, [(3, 3) (3, 4); (1, 3) (1, 4)])
            @test cell_vertices(CartesianIndex(1, 4), N) ==
                  map(CartesianIndex, [(1, 4) (1, 1); (2, 4) (2, 1)])
            @test cell_vertices(CartesianIndex(2, 4), N) ==
                  map(CartesianIndex, [(2, 4) (2, 1); (3, 4) (3, 1)])
