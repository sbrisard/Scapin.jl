            end
        end
    end
end

@testset "Brick element operators" begin
    for d = 2:3
        @testset "Brick element operators, $(d)d" begin
            # Note: reference values where computed with maxima
            # (see `ref/nd_brick_elasticity.mac`).
            h = [1.1, 1.2, 1.3][1:d]
